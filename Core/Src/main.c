  I2C_GPIO_Init();         // 初始化I2C相关GPIO
  MAX30102_Init();         // 初始化MAX30102血氧传感器
  error = Blood_Init();    // 初始化血氧测量模块
  ssd1306_Init();          // 初始化OLED显示屏
  MAX30205_Init();         // 初始化MAX30205温度传感器
